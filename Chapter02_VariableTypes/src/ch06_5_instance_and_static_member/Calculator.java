package ch06_5_instance_and_static_member;

public class Calculator {
	static double pi = 3.14159; // this is static and it's field.

	static int plus(int x, int y) { // this is static method
		return x + y;
	}
	static int minus(int x, int y) {// this is staic method.
		return x - y;
	}
}
