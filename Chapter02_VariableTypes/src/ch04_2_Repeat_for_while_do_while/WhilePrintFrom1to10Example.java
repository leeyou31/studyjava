package ch04_2_Repeat_for_while_do_while;

public class WhilePrintFrom1to10Example {

	public static void main(String[] args) {
		int i = 1; // we declared variable i has integer type and it has literal '1'
		while (i <= 10) { // while i is equal and smaller than 10, the i variable increases.
			i++;
			System.out.println(i); // when i want i to be printed, it shows 10 results that i is on increase.
	
		}

	}

}
