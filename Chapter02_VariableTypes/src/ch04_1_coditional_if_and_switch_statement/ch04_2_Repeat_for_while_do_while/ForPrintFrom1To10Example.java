package ch04_2_Repeat_for_while_do_while;

public class ForPrintFrom1To10Example {

	public static void main(String[] args) {
		for(int i=1; i<=10; i++) { // int i = 1 is the inital expression... then we put semi colon to finish the expression. after first inital expression, we put conditional statement expression, then lastly we put increase operater to finish this "for"statement
			System.out.println(i);
		}

	}

}
