package ch06_4_method;

public class PracticeTestCh06_4_Printer {
	static void println(int value) { // method is declared and there is no inner method.
		System.out.println(value); // and there is no return value.
	}
	
	static void println(boolean value) {
		System.out.println(value);
	}
	
	static void println(double value) {
		System.out.println(value);	
	}
	
	static void println(String value) {
		System.out.println(value);
	}
	
}
